/**
 * @brief User callback for modem event
 *
 *  This callback is called every time an event ( see modem_rsp_event_t ) appears in the modem.
 *  Several events may have to be read from the modem when this callback is called.
 */
static void get_event( void )
{
    BSP_DBG_TRACE_MSG_COLOR( "Get_event () callback\n", BSP_DBG_TRACE_COLOR_BLUE );

    modem_rsp_event_t type                   = RSP_NUMBER;
    uint8_t           count                  = 0;
    uint8_t           event_data[255]        = { 0 };
    uint8_t           event_data_length      = 0;
    uint8_t           asynchronous_msgnumber = 0;

    // Read modem event type and number
    modem_get_event( &type, &count, event_data, &event_data_length, &asynchronous_msgnumber );

    // Continue to read modem event until all events have been processed
    while( asynchronous_msgnumber > 0 )
    {
        BSP_DBG_TRACE_WARNING( "Event %u \n", type );
        switch( type )
        {
        case RSP_RESET:
            break;

        case RSP_ALARM:
            BSP_DBG_TRACE_INFO( " Alarm is finish, prepare a new file upload\n" );

            // Send alternatively file_0 and file_1 on port 25
            if( last_file_idx != 0 )
            {
                last_file_idx = 0;
                send_upload_file( file_0, sizeof( file_0 ), 25 );
            }
            else
            {
                last_file_idx = 1;
                send_upload_file( file_1, sizeof( file_1 ), 25 );
            }
            break;

        case RSP_JOINED:
            BSP_DBG_TRACE_INFO( "Modem is join, program an alarm\n" );
            modem_set_alarm_timer_s( bsp_rng_get_random_in_range( 45, 60 ) );
            break;

        case RSP_TXDONE:
            BSP_DBG_TRACE_INFO( "TX done \n" );
            break;

        case RSP_DOWNDATA:
            rx_payload_size = event_data_length - 4;
            memcpy( rx_payload, &event_data[4], rx_payload_size );
            BSP_DBG_TRACE_PRINTF( "Data received on port %u\n", event_data[3] );
            BSP_DBG_TRACE_ARRAY( "DOWNDATA", rx_payload, rx_payload_size );
            break;

        case RSP_FILEDONE:
            BSP_DBG_TRACE_INFO( "File upload is complete, program a new alarm \n" );
            modem_set_alarm_timer_s( bsp_rng_get_random_in_range( 60, 120 ) );
            break;

        case RSP_SETCONF:
            break;

        case RSP_MUTE:
            break;

        case RSP_LINKSTATUS:
            break;

        default:
            BSP_DBG_TRACE_ERROR( "Unknown event %u\n", type );
            break;
        }

        modem_get_event( &type, &count, event_data, &event_data_length, &asynchronous_msgnumber );
    }
}

/**
 * @brief Start an upload file
 *
 * @param file      Pointer to the file to send
 * @param file_size Size of the file
 * @param port      File upload port to use
 */
static void send_upload_file( uint8_t* file, uint16_t file_size, uint8_t port )
{
    BSP_DBG_TRACE_WARNING( "START A FILE UPLOAD (size:%d)\n", file_size );

    // Initialise the file upload with no encryption and a 2s average delay between each chunks
    modem_upload_init( port, FILE_UPLOAD_NOT_ENCRYPTED, file_size, 2 );
    modem_upload_start( file, file_size );
}
